    private void enterButtonArrayDeletionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonArrayDeletionActionPerformed
            textAreaArrayDeletion.setText("");
       
            int pos = (int) spinnerPosArrayDeletion.getValue();

            DataStore dataStore = DataStore.getInstance();
            int arrayList[] = dataStore.getData();

            int n = arrayList.length;
            int i;
           
            textAreaArrayDeletion.append("Array elements before deletion:\n\n");
            for(i = 0 ; i<n ; i++){
            textAreaArrayDeletion.append(" arrayList ["+ (i+1) +"]   =   "+arrayList[i]+"\n");
            }
            
            pos = pos - 1;

            if (pos >= 0 && pos < n) { 
             int[] newArr = new int[arrayList.length - 1];
             int destPos = 0;
                for (i = 0; i < n; i++) {
                    if (i != pos) { 
                      newArr[destPos++] = arrayList[i];
                    }
                }
                arrayList = newArr;
                
                n = arrayList.length;

               textAreaArrayDeletion.append("\nArray elements after deletion:\n\n");
               for(i = 0 ; i<n ; i++){
               textAreaArrayDeletion.append(" arrayList ["+ (i+1) +"]   =   "+arrayList[i]+"\n");
               }

               dataStore.setData(arrayList);
               javax.swing.JOptionPane.showMessageDialog(pageArray.this,"Deletion Complete!");
               }
            else {
                textAreaArrayDeletion.setText("");
                javax.swing.JOptionPane.showMessageDialog(pageArray.this," Position is out of bounds! ");               
            }
               
            
    }//GEN-LAST:event_enterButtonArrayDeletionActionPerformed

    private void enterButtonArrayInsertionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonArrayInsertionActionPerformed
            textAreaArrayInsertion.setText("");

            int data = (int) spinnerDataArrayInsertion.getValue();
            int pos = (int) spinnerPosArrayInsertion.getValue();

            DataStore dataStore = DataStore.getInstance();
            int arrayList[] = dataStore.getData();

            int n = arrayList.length;
            int i;
            int j = n;
            textAreaArrayInsertion.append("Array elements before insertion:\n\n");
                for(i = 0 ; i<n ; i++){
                     textAreaArrayInsertion.append(" arrayList ["+ (i+1) +"]   =   "+arrayList[i]+"\n");
                }

            if (pos > n) {
                textAreaArrayInsertion.setText("");
                javax.swing.JOptionPane.showMessageDialog(pageArray.this," Position is out of bounds! "); 
            } 
            else {
                if (n == arrayList.length) {
                    int[] newArr = new int[arrayList.length + 1];
                    for (i = 0; i < n; i++) {
                        newArr[i] = arrayList[i];
                    }
                    arrayList = newArr;
                }

                n++;
                for ( i = n - 1; i > pos-1; i--) {
                    arrayList[i] = arrayList[i - 1];
                }
                arrayList[pos - 1] = data;


                textAreaArrayInsertion.append("\nArray elements after insertion:\n\n");
                for(i = 0 ; i<n ; i++){
                    textAreaArrayInsertion.append(" arrayList ["+ (i+1) +"]   =   "+arrayList[i]+"\n");
                }
            javax.swing.JOptionPane.showMessageDialog(pageArray.this,"Insertion Complete!");    
            }
           dataStore.setData(arrayList);       
    
    }//GEN-LAST:event_enterButtonArrayInsertionActionPerformed

