    private void enterButtonArraySearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonArraySearchActionPerformed
            textAreaArraySearch.setText("");

            int data = (int) spinnerDataArraySearch.getValue();

            DataStore dataStore = DataStore.getInstance();
            int arrayList[] = dataStore.getData();

            int n = arrayList.length;
            int i;
            int j = 0;
            boolean found = false;
            textAreaArraySearch.append("Array elements:\n\n");
            for(i = 0 ; i<n ; i++){
                textAreaArraySearch.append(" arrayList ["+ (i+1) +"]   =   "+arrayList[i]+"\n");
            }

            textAreaArraySearch.append("\nElement to be searched: "+data+" \n\n");
            while (j < n) {
                if (arrayList[j] == data) {
                    found = true;
                    break;
                } 
                j++;
            }
       
            if (found) {
                textAreaArraySearch.append("Element " + data + " is found at position " + (j + 1));
                javax.swing.JOptionPane.showMessageDialog(pageArray.this,"Element found!");
            } else {
                textAreaArraySearch.append("Did not find the element " + data + " in the array.");
                javax.swing.JOptionPane.showMessageDialog(pageArray.this,"Element not found!");
            }
                               
    }//GEN-LAST:event_enterButtonArraySearchActionPerformed

