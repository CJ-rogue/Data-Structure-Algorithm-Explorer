    private void countActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_countActionPerformed
        textAreaStackDisplay.setText("");
        jTabbedPane1.setSelectedIndex(0);
        
        switch (top) {
            case -1:
                javax.swing.JOptionPane.showMessageDialog(pageStack.this,"No elements in the Stack!");
                break;
            case 0:
                javax.swing.JOptionPane.showMessageDialog(pageStack.this,"There is "+ (top+1) + " element in the Stack");
                break;
            default:
                javax.swing.JOptionPane.showMessageDialog(pageStack.this,"There are "+ (top+1) + " elements in the Stack");
                break;
        }
            
    }//GEN-LAST:event_countActionPerformed

    
    private void popActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_popActionPerformed
        textAreaStackDisplay.setText("");
        jTabbedPane1.setSelectedIndex(0);   

        if (top == -1) {  
            javax.swing.JOptionPane.showMessageDialog(pageStack.this,"Underflow!");
        } 
        
        else {  
            int poppedValue = data[top];
            top--;
            javax.swing.JOptionPane.showMessageDialog(pageStack.this,"Item popped! Value = " + poppedValue);
        }       

            data[top+1] = 0;
            dataStore.setData(data);
     
    }//GEN-LAST:event_popActionPerformed

    private void enterButtonStackPushActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enterButtonStackPushActionPerformed
    
        int dataPush = (int) spinnerDataPush.getValue();
        
        
    if (top == maxsize) {  
        javax.swing.JOptionPane.showMessageDialog(pageStack.this,"Overflow!");
    } else {  
        
        top++;
        data[top] = dataPush;
  
        javax.swing.JOptionPane.showMessageDialog(pageStack.this,"Item pushed! Value = " + dataPush);

        // Update the data store with the modified array
        dataStore.setData(data);
    }  
        
    }//GEN-LAST:event_enterButtonStackPushActionPerformed

