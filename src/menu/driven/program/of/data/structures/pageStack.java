    private void displayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayActionPerformed
    textAreaStackDisplay.setText("");
    jTabbedPane1.setSelectedIndex(1);

    for (int i=top ; i>=0; i--) {  
        String element = " " + data[i] + " "; // add spaces for padding
        String dashes = "-".repeat(element.length() + 2); // create dashes for top and bottom
        String formattedOutput = String.format("\t%s\n\t|%s|\n\t%s\n", dashes, element, dashes);
        if (i > 0) {
            formattedOutput += "\t  ↑\n";
        }
        textAreaStackDisplay.append(formattedOutput);
    }  
    
    if(top == -1) {  
        javax.swing.JOptionPane.showMessageDialog(pageStack.this,"Stack is Empty!");
    }  
    }//GEN-LAST:event_displayActionPerformed

